#logical operators are used to combine two or more conditions and give a single output(it's basically the three main logic gates)
#the three main logic gates are 'and', 'or' and 'not'
#'and' is used to join two statements and only gives output if both statements are true
#'or' is used to join two statements and gives output if at least one of the statements is true
#'not' is used to reverse the output of a statement(remember not gate)
